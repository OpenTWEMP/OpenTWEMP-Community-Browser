namespace TWEMP.Browser.Core.CommonLibrary.CustomManagement.Gaming;

/// <summary>
/// Defines available types of game engines supported by the application.
/// </summary>
public enum GameEngineSupportType
{
    /// <summary>
    /// Use this type to support the OpenTWEMP Game Engine.
    /// </summary>
    TWEMP,

    /// <summary>
    /// Use this type to support CA's Medieval II Total War game engine.
    /// </summary>
    M2TW,

    /// <summary>
    /// Use this type to support CA's  Rome Total War game engine.
    /// </summary>
    RTW,
}
